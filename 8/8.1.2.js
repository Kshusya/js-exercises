// Дан массив:

// [
// 	[2, 4, 5],
// 	[1, 2, 3],
// 	[0, 1, 1],
// 	[5, 7, 1],
// ]
// Отсортируйте элементы массива по возрастанию суммы элементов подмассива.

const arr = [
	[2, 4, 5],
	[1, 2, 3],
	[0, 1, 1],
	[5, 7, 1],
]

arr.sort((a, b) => a.reduce((acc, value) => acc + value, 0) - b.reduce((acc, value) => acc + value, 0));

