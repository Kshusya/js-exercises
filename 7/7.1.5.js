// 		[1, 2, 3],
// 	],
// ]

let arr = []
for (let i = 1; i <= 3; i++) {
	let arr1 = [];

	for (let j = 1; j <= 3; j++) {
		let arr2 = [];

		for (let k = 1; k <= 3; k++) {
			arr2.push(k);
		}

		arr1.push(arr2);
	}
	
	arr.push(arr1);
}

